# 1. Initializing a string and printing it
message = "Hello, World!"
print(message)

# 2. Storing and printing a name
name = "Aisha"
print("My name is", name)

# 3. Storing and printing an age
age = 20
print("I am", age, "years old.")

# 4. Displaying numeric values  
print(2024)  
print(2.718) 

# 5. Storing and printing a hobby
hobby = "Reading"
print("I love", hobby)

# 6. Dictionary variable
person = {"name": "Aisha", "age": 20, "city": "Gujranwala"}
print("Person details:", person)

# 7. Storing and printing a sentence
sentence = "Python is easy to learn."
print(sentence)

# 8. Set variable
unique_numbers = {1, 2, 3, 4, 5}
print("Unique numbers:", unique_numbers)

# 9. Printing a formatted string
fruit = "Mango"
print("I like to eat {}.".format(fruit))

# 10. Storing and printing a multi-line string
quote = """Success is not final,
Failure is not fatal,
It is the courage to continue that counts."""
print(quote)
