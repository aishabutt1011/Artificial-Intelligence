# First loop to print increasing pattern of stars
for i in range(1, 6):  # Loop from 1 to 5
    print("*" * i)     # Print i stars in each line

# Second loop to print decreasing pattern of stars
for i in range(4, 0, -1):  # Loop from 4 to 1 (reverse order)
    print("*" * i)         # Print i stars in each line
